//5. Conditionals/ if...else..if
// dekalaralok egy valtozot egy evszakkal es ezt hasonlitom ossze kulonbozo kondiciokkal
let season = 'summer';
// a kondiciok, osszehasonlitom mindegyik kondicioval az ertekemet es amelyik true azt irom ki/adom vissza
if (season === 'spring') {
    console.log(`It\'s spring!`)
} else if (season === 'summer') {
    console.log(`It\'s summer!`)
} else if (season === 'fall') {
    console.log(`It\'s fall!`)
} else if (season === 'winter') {
    console.log(`It\'s winter!`)
} else {console.log('Invalid season')};